<h1>pOWL-WCMS</h1>

        <p>This module is a prototypically developed Semantic Web Content Management System based on pOWL.
           As key requirement to modern Content Management systems is the separation of structure, content and style.
           This is realized by defining interconnected ontologies for the page structure of the web site, for text and multimedia content
           and rendering style information.</p>

<h2>Features</h2>
<ul>
<li>Separation of content, layout and structure.</li>
<li>Light workflow management.</li>
<li>Import content from Bibtext, VCal or VCard.</li>
<li>Export content into Bibtext, VCal or VCard.</li>
</ul>

